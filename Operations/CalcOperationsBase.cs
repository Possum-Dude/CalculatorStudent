namespace CalculatorStudent.Operations
{
    public class CalcOperationsBase
    {

        public static double Add(double Number1d, double Number2d)
        {
            return Number1d + Number2d;
        }

        public static double Divide(double Number1d, double Number2d)
        {
            return Number1d / Number2d;
        }

        public static double Multiply(double Number1d, double Number2d)
        {
            return Number1d * Number2d;
        }

        public static double Subtract(double Number1d, double Number2d)
        {
            return Number1d - Number2d;
        }
    }
}